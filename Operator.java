public class Operator {
    public static void main(String[] args){
        int x = 2, y = 3; 

        System.out.println(x + " + " + y + " = " +(x+y));
        System.out.println(x + " - " + y + " = " +(x-y));
        System.out.println(x + " * " + y + " = " +(x*y));
        System.out.println(x + " / " + y + " = " +(x/y)); //การหาร
        System.out.println(x + " % " + y + " = " +(x%y)); //การหาเศษที่ได้จากการหาร

    }
}
